package Operator;

public class Arithmetic {
    public static void main(String[] args) {

        float a = 7.0f;
        float b = 4.0f;

        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);
        System.out.println(a%b);
    }
}
